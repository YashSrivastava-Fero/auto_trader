// Generate latest rates
export const latestRates = Array.from({ length: 100 }, (_, i) => {
  const open = Math.random() * 2000 + 100
  const variation = open * 0.05
  return {
    scrip:
      scripCodes[i % scripCodes.length] + (i >= scripCodes.length ? String(Math.floor(i / scripCodes.length)) : ""),
    open,
    high: open + Math.random() * variation,
    low: open - Math.random() * variation,
    close: open + (Math.random() - 0.5) * variation,
    ltp: open + (Math.random() - 0.5) * variation,
    date: `2025-01-${String(Math.floor(Math.random() * 25) + 1).padStart(2, "0")}`,
  }
})

