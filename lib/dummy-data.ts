// Generate scrip list
export const scripList = Array.from({ length: 200 }, (_, i) => {
  const code =
    scripCodes[i % scripCodes.length] + (i > scripCodes.length ? String(Math.floor(i / scripCodes.length)) : "")
  return {
    scripCode: code,
    scripName: `${code} Limited`,
    isin: `INE${String(Math.floor(Math.random() * 999999)).padStart(6, "0")}01`,
    segment: Math.random() > 0.5 ? "NSE" : "BSE",
    status: "Active",
  }
})

